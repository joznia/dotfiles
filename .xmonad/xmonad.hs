-- Layouts
myLayout = avoidStruts (tiled ||| Mirror tiled ||| Full)
  where
     -- Add gaps
     tiled   = smartSpacing 5 $ Tall nmaster delta ratio
     -- The default number of windows in the master pane
     nmaster = 1
     -- Default proportion of screen occupied by master pane
     ratio   = 1/2
     -- Percent of screen to increment by when resizing panes
     delta   = 5/100

-- Window rules
myManageHook = composeAll
    [ className =? "MPlayer"        --> doFloat
    , className =? "Gimp"           --> doFloat
    , className =? "feh"            --> doFloat
    , resource  =? "desktop_window" --> doIgnore
    , resource  =? "kdesktop"       --> doIgnore ]

-- Event handling
myEventHook = mempty

-- Run autostart script
myStartupHook = do
        spawnOnce "/bin/bash /home/jz/.xmonad/autostart.sh"

-- Run xmonad with our defaults
main = do
  -- Run xmobar
  xmproc0 <- spawnPipe "xmobar -x 0 /home/jz/.config/xmobar/xmobarrc0"
  xmproc1 <- spawnPipe "xmobar -x 1 /home/jz/.config/xmobar/xmobarrc0"
  -- Finally, run xmonad
  xmonad $ docks def
      {
      -- simple stuff
        terminal           = myTerminal,
        focusFollowsMouse  = myFocusFollowsMouse,
        clickJustFocuses   = myClickJustFocuses,
        borderWidth        = myBorderWidth,
        modMask            = myModMask,
        workspaces         = myWorkspaces,
        normalBorderColor  = myNormalBorderColor,
        focusedBorderColor = myFocusedBorderColor,

      -- key bindings
        keys               = myKeys,
        mouseBindings      = myMouseBindings,

      -- hooks, layouts
        layoutHook         = myLayout,
        manageHook         = myManageHook,
        handleEventHook    = myEventHook,
        logHook            = dynamicLogWithPP xmobarPP
                                { ppOutput = \x -> hPutStrLn xmproc0 x >> hPutStrLn xmproc1 x
                                , ppTitle = xmobarColor "purple" "" . shorten 50
                                },

        startupHook        = myStartupHook
    }

